package Homework;

public class PracticeOperatorsHW3 {

    public static void main(String[] args) {

        int a = 7;
        int b = 12;
        int sum = a + b;
        System.out.println("The sum of 7 and 12 is " + sum);

//////////////////////////////////////////////////////////////////

        int a1 = 15;
        int b1 = 8;
        int sum1 = a1 -b1;
        System.out.println("The difference between 15 and 8 is " + sum1);

//////////////////////////////////////////////////////////////////

        int a2 = 3;
        int b2 = 5;
        int c2 = 7;
        int sum2 = a2 * b2 * c2;
        System.out.println("The product of 3, 5, and 7 is " + sum2);

///////////////////////////////////////////////////////////////////

        int a3 = 20;
        int b3 = 5;
        int sum3 = a3 / b3;
        System.out.println("The division of 20 by 5 is " + sum3);

///////////////////////////////////////////////////////////////////

        int a4 = 27;
        int b4 = 4;
        int sum4 = a4 % b4;
        System.out.println("The remainder of 27 divided by 4 is " + sum4);

///////////////////////////////////////////////////////////////////

        int a5 = 2;
        int b5 = 5;
        int sum5 = (a5 + b5) * b5 - 3;
        System.out.println("2 raised to the power of 5 is " + sum5);

//////////////////////////////////////////////////////////////////////

        int a6 = 10;
        int b6 = 15;
        int c6 = 20;
        int sum6 = (a6 + b6 + c6) / 3;
        System.out.println("The average of 10, 15, and 20 is " + sum6);

//////////////////////////////////////////////////////////////////////

        int a7 = 5;
        int b7 = 8;
        int sum7 = a7 * b7;
        System.out.println("The area of a rectangle with length 5 and width 8 is " + sum7);

//////////////////////////////////////////////////////////////////////

        int a8 = 6;
        int sum8 = a8 * 4;
        System.out.println("The perimeter of a square with side length 6 is " + sum8);
    }
}
