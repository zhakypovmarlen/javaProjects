package IfElse;

public class IfElseIf {
    public static void main(String[] args) {

        if (2 > 5){
            System.out.println("2 is less than 5");
        } else if(2 < 5){
            System.out.println("2 is more than 5");
        } else{
            System.out.println("2 is equal to 5");
        }















    }
}
